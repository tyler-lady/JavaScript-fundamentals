// Assignment Operator
let r = 5;

// Addition assignment operator
r = r + 1;
r += 1;
r++; //adds 1

// Subtraction assignment operator
r -= 3;
r--; //subtracts 1

//Multiplication operator
r *= 10;

//Division operator
r /= 3;

//Exponential operator
r **= 3;
r ** 3;

//Modulus operator
r %= 5;
//r divide by 5 and tell us remainder

